a = [0x99, 0xB0, 0x87, 0x9E, 0x70, 0xE8, 0x41, 0x44, 5, 4, 0x8B, 0x9A, 0x74, 0xBC, 0x55, 0x58, 0xB5, 0x61, 0x8E, 0x36,
     0xAC, 9, 0x59, 0xE5, 0x61, 0xDD, 0x3E, 0x3F, 0xB9, 0x15, 0xED, 0xD5]
flag = ""
for x in range(4):
    for i in range(31, 0, -1):
        a[i] = a[i] ^ a[i - 1]
for i in range(0, len(a)):
    for x in range(32, 128):
        if ((x * 4 | x >> 6) ^ i) & 0xff == a[i]:
            flag = flag + chr(x)
print(flag)
